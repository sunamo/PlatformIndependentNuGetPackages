namespace SunamoSharedMisc._public;

/// <summary>
/// Specifies the type of bracket characters.
/// </summary>
public enum BracketsShared
{
    /// <summary>
    /// Curly brackets: { }
    /// </summary>
    Curly,
    /// <summary>
    /// Square brackets: [ ]
    /// </summary>
    Square,
    /// <summary>
    /// Normal (round) brackets: ( )
    /// </summary>
    Normal,
    /// <summary>
    /// No brackets.
    /// </summary>
    None
}