namespace SunamoSharedMisc;

/// <summary>
/// Represents supported video file format extensions.
/// </summary>
public enum VideoExtension
{
    /// <summary>
    /// MPEG-4 Part 14 video format (.mp4).
    /// </summary>
    Mp4,

    /// <summary>
    /// Flash Video format (.flv).
    /// </summary>
    Flv,

    /// <summary>
    /// No video extension specified.
    /// </summary>
    None
}