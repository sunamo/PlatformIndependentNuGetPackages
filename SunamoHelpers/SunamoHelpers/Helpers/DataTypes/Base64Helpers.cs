namespace SunamoHelpers.Helpers.DataTypes;

/// <summary>
/// Provides constants for Base64 data URI prefixes used with common image formats.
/// </summary>
public class Base64Helpers
{
    /// <summary>
    /// Base64 data URI prefix for JPEG images.
    /// </summary>
    public const string Jpeg = "data:image/jpeg;base64,";

    /// <summary>
    /// Base64 data URI prefix for PNG images.
    /// </summary>
    public const string Png = "data:image/png;base64,";
}
